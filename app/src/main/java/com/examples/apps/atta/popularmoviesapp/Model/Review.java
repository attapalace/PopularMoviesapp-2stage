public class Review {
    public String id;
    public String author;
    public String content;
    public String url;

    public Review() {
    }

    public String getId() {
        return id;
    }

    public String getAuthor() {
        return author;
    }

    public String getContent() {
        return content;
    }

    public String getUrl() {
        return url;
    }
}
