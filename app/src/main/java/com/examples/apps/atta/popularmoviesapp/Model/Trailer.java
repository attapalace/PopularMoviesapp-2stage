public class Trailer {

    public String id;
    public String key;
    public String name;
    public String site;
    public int size;
    public String type;

    public Trailer() {
    }

    public String getId() {
        return id;
    }

    public String getKey() {
        return key;
    }

    public String getName() {
        return name;
    }

    public String getSite() {
        return site;
    }

    public int getSize() {
        return size;
    }

    public String getType() {
        return type;
    }
}
